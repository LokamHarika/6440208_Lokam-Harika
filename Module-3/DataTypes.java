public class DataTypes {
    public static void main(String[] args) {
        int i = 25;
        float f = 12.5f;
        double d = 123.456;
        char c = 'A';
        boolean b = true;

        System.out.println("Integer: " + i);
        System.out.println("Float: " + f);
        System.out.println("Double: " + d);
        System.out.println("Character: " + c);
        System.out.println("Boolean: " + b);
    }
}
